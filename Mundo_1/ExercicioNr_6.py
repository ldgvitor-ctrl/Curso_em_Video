n = int(input("Digite um número inteiro positivo: "))
dobro = n * 2 # Calcula o dobro do número
triplo = n * 3 # Calcula o triplo do número
raiz_quadrada = n ** 0.5 # Calcula a raiz quadrada do número
print(f"O dobro de {n} é {dobro}.")
print(f"O triplo de {n} é {triplo}.")
print(f"A raiz quadrada de {n} é {raiz_quadrada:.2f}.")