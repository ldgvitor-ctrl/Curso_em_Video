salario = float(input("Digite o salário do funcionário: R$ "))
aumento = salario * 0.15 # Cálculo de 15% de aumento
novo_salario = salario + aumento # Novo salário após o aumento
print(f"O novo salário, com 15% de aumento, é: R$ {novo_salario:.2f}")
print(f"O valor do aumento foi: R$ {aumento:.2f}")